/**
 * AppEvent — перечисление всех событий, которые используются в приложении
 * для взаимодействия между слоями модели, представления и логики (MVP).
 */
export enum AppEvent {
	/**
	 * Событие: каталог товаров был обновлён.
	 * Используется при загрузке данных с сервера.
	 */
	CATALOG_CHANGED = 'catalog:changed',

	/**
	 * Событие: содержимое корзины изменилось.
	 * Используется при добавлении или удалении товаров из корзины.
	 */
	CART_CHANGED = 'cart:changed',

	/**
	 * Событие: данные заказа обновлены.
	 * Вызывается при изменении полей в форме (адрес, оплата, контакты).
	 */
	ORDER_UPDATED = 'order:updated',

	/**
	 * Событие: пользователь отправил форму заказа.
	 */
	ORDER_SUBMIT = 'order:submit',

	/**
	 * Событие: заказ успешно оформлен.
	 * Показывает сообщение об успешной оплате и сбрасывает состояние корзины.
	 */
	ORDER_SUCCESS = 'order:success',

	/**
	 * Событие: товар добавлен в корзину.
	 */
	ORDER_ADD_PRODUCT = 'order:add-product',

	/**
	 * Событие: товар удалён из корзины.
	 */
	ORDER_REMOVE_PRODUCT = 'order:remove-product',

	/**
	 * Событие: пользователь не заполнил адрес или способ оплаты.
	 * Используется для отображения ошибок в форме доставки.
	 */
	ORDER_DELIVERY_REQUIRED = 'order:delivery-required',

	/**
	 * Событие: пользователь не заполнил Email или телефон.
	 * Используется для отображения ошибок в форме контактов.
	 */
	ORDER_CONTACTS_REQUIRED = 'order:contacts-required',

	/**
	 * Событие: пользователь выбрал товар для предпросмотра.
	 * Используется, например, для установки состояния предпросмотра.
	 */
	PRODUCT_PREVIEW = 'product:preview',

	/**
	 * Событие: открыто модальное окно предпросмотра товара.
	 */
	PRODUCT_PREVIEW_OPEN = 'product:preview-open',
	/**
    * Событие: модель ответила результатом валидации полей заказа.
    * Передаёт { isValid: boolean; errors: Record<string, string> }.
    */
	ORDER_FORM_VALIDITY_CHANGED = 'order:form-validity-changed'
}
